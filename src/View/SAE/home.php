    <h2 class="remplaceBaliseLegend">Les Alternances</h2>
    <br>
    <p>Vous pouvez réaliser votre BUT en alternance dès la seconde année.</p>
    <p>Votre alternance peut débuter :</p>
    <p>au semestre 3: mais le peu de temps passé en entreprise ce semestre ne rend pas cette date très prisée par les
        entreprises. Vous pouvez qd même tenter votre chance ;-)</p>
    <p>au semestre 4: le rythme est apprécié par les entreprises. L&#8217;alternance vient en remplacement du stage. C&#8217;est
        l&#8217;idéal (*)</p>
    <p>au semestre 5: très bien car le rythme du BUT3 est conçu pour l&#8217;alternance.</p>
    <p>Une fois signé le contrat dure jusqu&#8217;à la fin du BUT3.</p>
    <p>(*) Etre alternant ne vous fera rater aucun cours. Vous serez uniquement dispensés de certaines SAE.</p>
