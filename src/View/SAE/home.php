    <h3>Rémunération</h3>
    <br>
    <p>La rémunération minimale réglementaire d’un apprenti·e est basée sur trois critères :</p>
    <p>Le critère principal est celui de l’<strong>année contractuelle</strong> &#8211; année d’exécution du contrat ;
    </p>
    <p>La <strong>tranche d’âge de l’apprenti au moment de l’embauche, et son évolution</strong> dans le temps, le cas
        échéant ;</p>
    <p>Son <strong>évolution dans le cycle de formation suivie</strong> : en principe, d’une durée désormais de 6 mois à
        3 ans maximum.</p>
    <p>La rémunération dépend des dispositions conventionnelles de l’entreprise, à défaut, c’est le salaire minimum de
        croissance en vigueur (SMIC).</p>
